"""
Контекст выполнения запроса.

Требования:
- Документирование всех методов.
- Текст исколючений писать на русском.
"""
from typing import Optional, List

class Context:
    """
    Контекст выполнения запроса.

    Содержит информацию о пользователе, инициировавшем действие, и его ролях.
    Не содержит result — результат передаётся через цепочку аспектов отдельным параметром,
    что делает действия stateless и упрощает тестирование.

    Атрибуты:
        user_id (Optional[str]): идентификатор пользователя (может быть None для гостя).
        roles (List[str]): список ролей пользователя (по умолчанию пустой).
    """
    def __init__(self, user_id: Optional[str] = None, roles: Optional[List[str]] = None):
        """
        Инициализирует контекст.

        Параметры:
            user_id: идентификатор пользователя (строка или None).
            roles: список ролей (если не указан, будет пустой список).
        """
        self.user_id = user_id
        self.roles = roles if roles is not None else []