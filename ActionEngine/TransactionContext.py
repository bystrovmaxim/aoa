"""
Расширенный контекст для действий, работающих с транзакциями.

Требования:
- Документирование всех классов.
- Документирование всех методов.
- Текст исключений писать на русском.
"""
from .Context import Context

class TransactionContext(Context):
    """
    Расширенный контекст выполнения для действий, работающих с транзакциями.

    Добавляет поле connection, которое содержит открытое соединение (например, с БД или Kafka).
    Это соединение передаётся во все аспекты через контекст, позволяя им выполнять
    операции в рамках одной транзакции.

    Конструктор принимает базовый контекст (Context или его подкласс) и соединение,
    и копирует все поля базового контекста в новый экземпляр. Это гарантирует,
    что при добавлении новых полей в базовый контекст не потребуется обновлять
    код создания TransactionContext.

    Атрибуты:
        connection: объект соединения (тип зависит от конкретной реализации).
    """

    def __init__(self, base_ctx: Context, connection=None):
        """
        Инициализирует транзакционный контекст.

        Параметры:
            base_ctx: исходный контекст (обычно экземпляр Context или его подкласс).
            connection: открытое соединение (или None, если ещё не открыто).
        """
        # Копируем все атрибуты из базового контекста в self
        self.__dict__.update(base_ctx.__dict__)
        self.connection = connection