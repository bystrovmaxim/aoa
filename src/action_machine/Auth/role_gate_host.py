# src/action_machine/Auth/role_gate_host.py
"""
RoleGateHost – миксин для присоединения шлюза ролей к классу действия.

Этот миксин используется в иерархии BaseAction. Он:
- Создаёт экземпляр RoleGate для класса (один на класс, разделяется всеми экземплярами).
- Собирает информацию о ролевой спецификации из декоратора @CheckRoles, применённого к классу.
- После сбора замораживает шлюз, чтобы гарантировать неизменность спецификации.
- Предоставляет метод get_role_gate() для доступа к шлюзу из машины.

Механизм сбора:
    Декоратор @CheckRoles при применении к классу добавляет в класс временный атрибут
    __role_info (объект RoleInfo). В __init_subclass__ этот атрибут собирается
    и регистрируется в шлюзе. После регистрации временный атрибут удаляется.

Обратная совместимость:
    На время миграции старый атрибут _role_spec продолжает существовать
    и заполняется параллельно. После полного перехода на шлюзы старый атрибут
    будет удалён.
"""

from typing import Any, ClassVar

from .role_gate import RoleGate, RoleInfo


class RoleGateHost:
    """
    Миксин, добавляющий классу шлюз ролей.

    Классовые атрибуты:
        __role_gate: RoleGate | None – шлюз, общий для всех экземпляров.
        __role_info: RoleInfo | None – временное хранилище, используемое декоратором
                     @CheckRoles для передачи данных в __init_subclass__.
    """

    __role_gate: ClassVar[RoleGate | None] = None
    __role_info: ClassVar[RoleInfo | None] = None

    @classmethod
    def get_role_gate(cls) -> RoleGate:
        """
        Возвращает шлюз ролей для данного класса.

        Шлюз создаётся лениво при первом вызове, если ещё не был создан.
        После завершения __init_subclass__ шлюз замораживается.

        Возвращает:
            RoleGate, связанный с классом.
        """
        if cls.__role_gate is None:
            cls.__role_gate = RoleGate()
        return cls.__role_gate

    def __init_subclass__(cls, **kwargs: Any) -> None:
        """
        Вызывается при создании подкласса. Собирает ролевую спецификацию
        из временных метаданных и регистрирует её в шлюзе.

        Алгоритм:
            1. Вызывает super().__init_subclass__() для поддержки множественного наследования.
            2. Получает шлюз через get_role_gate().
            3. Если есть __role_info (временные данные от декоратора @CheckRoles),
               регистрирует его в шлюзе.
            4. Удаляет __role_info, чтобы не засорять класс.
            5. Замораживает шлюз.

        Аргументы:
            **kwargs: передаются в родительский __init_subclass__.
        """
        super().__init_subclass__(**kwargs)

        gate = cls.get_role_gate()

        # Собираем роль, накопленную декоратором
        if cls.__role_info is not None:
            gate.register(cls.__role_info)

        # Очищаем временные данные
        if hasattr(cls, "__role_info"):
            delattr(cls, "__role_info")

        # Замораживаем шлюз – после этого регистрация невозможна
        gate.freeze()