"""
Contrib-пакеты ActionMachine с опциональными зависимостями.

═══════════════════════════════════════════════════════════════════════════════
PURPOSE
═══════════════════════════════════════════════════════════════════════════════

Содержит конкретные реализации адаптеров и менеджеров ресурсов для различных
протоколов и технологий. Каждый адаптер устанавливается как опциональная
зависимость:

    pip install action-machine[fastapi]
    pip install action-machine[mcp]
    pip install action-machine[postgres]

Адаптеры преобразуют внешние протоколы в вызовы
``machine.run(context, action, params, connections)`` и используют
базовую инфраструктуру из ``action_machine.adapters``.

═══════════════════════════════════════════════════════════════════════════════
ДОСТУПНЫЕ АДАПТЕРЫ
═══════════════════════════════════════════════════════════════════════════════

- action_machine.contrib.fastapi — FastAPI-адаптер. Превращает Action
  в HTTP-эндпоинты с автоматической генерацией OpenAPI-документации
  из метаданных Pydantic-моделей и декоратора @meta.

- action_machine.contrib.mcp — MCP-адаптер. Превращает Action
  в MCP tools для AI-агентов (Claude, ChatGPT, Cursor). Генерирует
  inputSchema из Pydantic-моделей, description из @meta. Регистрирует
  resource system://graph со структурой системы.

═══════════════════════════════════════════════════════════════════════════════
ДОСТУПНЫЕ МЕНЕДЖЕРЫ РЕСУРСОВ
═══════════════════════════════════════════════════════════════════════════════

- action_machine.contrib.postgres — менеджер соединений PostgreSQL
  на базе asyncpg. Реализует SqlConnectionManager с methodами
  open/begin/commit/rollback/execute.

═══════════════════════════════════════════════════════════════════════════════
ARCHITECTURE / DATA FLOW
═══════════════════════════════════════════════════════════════════════════════

    ┌─────────────────────────────────────────────────────┐
    │  Внешний протокол                                   │
    │  (HTTP, MCP, PostgreSQL)                            │
    └──────────────────────┬──────────────────────────────┘
                           │
                           ▼
    ┌─────────────────────────────────────────────────────┐
    │  contrib адаптер / менеджер                          │
    │  (FastApiAdapter, McpAdapter, PostgresManager)       │
    └──────────────────────┬──────────────────────────────┘
                           │
                           │  machine.run(context, action, params, connections)
                           ▼
    ┌─────────────────────────────────────────────────────┐
    │  ActionProductMachine (ядро)                         │
    └─────────────────────────────────────────────────────┘
"""
