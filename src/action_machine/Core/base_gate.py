# src/action_machine/Core/base_gate.py
"""
Абстрактный базовый класс для всех шлюзов (Gates) в ActionMachine.

Шлюз — это контейнер, который хранит компоненты определённого типа.
Он предоставляет единый интерфейс для регистрации, удаления и получения
компонентов. Этот дизайн заменяет магические атрибуты явным,
типобезопасным и тестируемым API.

Все конкретные шлюзы наследуются от BaseGate и параметризуются типом
компонента T. Они могут добавлять дополнительные методы для удобного
доступа к компонентам (например, get_by_class, get_role_spec и т.д.).

Шлюзы могут быть заморожены после инициализации (см. флаг _frozen в реализациях),
чтобы гарантировать неизменность набора компонентов после сборки.

Параметр типа:
    T: тип компонента, которым управляет шлюз.

Методы:
    register(component, **metadata): добавляет компонент.
    unregister(component): удаляет компонент.
    get_components(): возвращает список всех компонентов в порядке регистрации.
"""

from abc import ABC, abstractmethod
from typing import Any, TypeVar

T = TypeVar('T')


class BaseGate[T](ABC):
    """
    Абстрактный базовый класс для всех шлюзов.

    Все шлюзы должны реализовывать эти три метода. Конкретные шлюзы
    могут добавлять дополнительные методы, специфичные для типа компонентов.
    """

    @abstractmethod
    def register(self, _component: T, **metadata: Any) -> T:
        """
        Зарегистрировать компонент в шлюзе.

        Аргументы:
            _component: регистрируемый компонент (типа T).
            **metadata: дополнительные метаданные (описание, тип, и т.п.).
                        Например, для CheckerGate требуется target_type и method.

        Возвращает:
            Зарегистрированный компонент (для удобства цепочек).

        Исключения:
            ValueError: если компонент с таким ключом уже зарегистрирован
                        (там, где это имеет смысл).
            RuntimeError: если шлюз уже заморожен (в конкретной реализации).
        """
        pass

    @abstractmethod
    def unregister(self, _component: T) -> None:
        """
        Удалить компонент из шлюза.

        Аргументы:
            _component: компонент, который нужно удалить.

        Исключения:
            RuntimeError: если шлюз уже заморожен (в конкретной реализации).
        """
        pass

    @abstractmethod
    def get_components(self) -> list[T]:
        """
        Вернуть все зарегистрированные компоненты.

        Возвращаемый список должен быть копией внутреннего хранилища,
        чтобы предотвратить внешние модификации. Порядок элементов
        должен соответствовать порядку регистрации, если он важен.

        Возвращает:
            Список компонентов в порядке регистрации.
        """
        pass