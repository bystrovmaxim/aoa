# src/action_machine/plugins/plugin_event.py
"""
Модуль, определяющий класс PluginEvent — контейнер для передачи данных
в обработчики плагинов.

═══════════════════════════════════════════════════════════════════════════════
НАЗНАЧЕНИЕ
═══════════════════════════════════════════════════════════════════════════════

PluginEvent используется координатором плагинов (PluginCoordinator) для
упаковки всех параметров, связанных с событием, которые затем передаются
в методы плагинов, помеченные декоратором @on.

Поля класса предназначены только для чтения. Плагины не должны изменять
объект события, так как это может повлиять на выполнение основного действия.

═══════════════════════════════════════════════════════════════════════════════
ТИПЫ СОБЫТИЙ
═══════════════════════════════════════════════════════════════════════════════

    global_start      — начало выполнения действия.
    before:{aspect}   — перед выполнением аспекта.
    after:{aspect}    — после выполнения аспекта.
    on_error          — ошибка в аспекте (до вызова @on_error обработчика Action).
    global_finish     — завершение выполнения действия.

═══════════════════════════════════════════════════════════════════════════════
СОБЫТИЕ on_error
═══════════════════════════════════════════════════════════════════════════════

Событие "on_error" эмитируется ПЕРЕД вызовом обработчика @on_error
на уровне Action. Плагин-наблюдатель не может изменить результат
или подавить ошибку — он только наблюдает. Поля PluginEvent при on_error:

    event_name          = "on_error"
    action_name         = полное имя действия
    params              = входные параметры
    state_aspect        = состояние конвейера на момент ошибки
    error               = экземпляр исключения, возникшего в аспекте
    has_action_handler  = True если Action имеет подходящий @on_error обработчик
    result              = None (ещё не определён)
    duration            = None

═══════════════════════════════════════════════════════════════════════════════
ПРИМЕР ПЛАГИНА-НАБЛЮДАТЕЛЯ
═══════════════════════════════════════════════════════════════════════════════

    class ErrorObserverPlugin(Plugin):
        async def get_initial_state(self) -> dict:
            return {"errors": []}

        @on("on_error", ".*")
        async def observe_errors_on(self, state, event, log):
            state["errors"].append({
                "action": event.action_name,
                "error": str(event.error),
                "has_handler": event.has_action_handler,
            })
            await log.error(
                "[{%scope.plugin}] Ошибка в {%scope.action}: {%var.error}",
                error=str(event.error),
            )
            return state
"""

from dataclasses import dataclass

from action_machine.context.context import Context
from action_machine.core.protocols import ReadableDataProtocol, WritableDataProtocol
from action_machine.dependencies.dependency_factory import DependencyFactory


@dataclass
class PluginEvent:
    """
    Контейнер для всех данных, передаваемых в обработчик плагина.

    Создаётся на каждое событие и передаётся в методы плагинов,
    помеченные декоратором @on.

    Все поля предназначены только для чтения; изменение объекта
    не влияет на выполнение действия (плагины read-only).
    """

    event_name: str
    """Имя события ('global_start', 'before:validate', 'after:save', 'on_error', 'global_finish')."""

    action_name: str
    """Полное имя класса действия (включая модуль), для которого произошло событие."""

    params: ReadableDataProtocol
    """Входные параметры действия (неизменяемый объект, соответствующий ReadableDataProtocol)."""

    state_aspect: dict[str, object] | None
    """
    Состояние (state) на момент события.
    Для before-событий — состояние до выполнения аспекта.
    Для after-событий — состояние после выполнения аспекта.
    Для on_error — состояние на момент возникновения ошибки.
    Для global_start и global_finish может быть None или содержать финальное состояние.
    """

    is_summary: bool
    """Флаг, указывающий, относится ли событие к summary-аспекту (если применимо)."""

    deps: DependencyFactory
    """Фабрика зависимостей для текущего выполнения действия (позволяет получать ресурсы)."""

    context: Context
    """Контекст выполнения (информация о пользователе, запросе, окружении)."""

    result: WritableDataProtocol | None
    """
    Результат выполнения действия (для событий global_finish).
    Для остальных событий — None.
    """

    duration: float | None
    """
    Длительность выполнения в секундах.
    Для after-событий — время выполнения соответствующего аспекта.
    Для global_finish — общее время выполнения действия.
    Для других событий — None.
    """

    nest_level: int
    """Уровень вложенности вызова действия (0 для корневого действия, 1 для дочернего и т.д.)."""

    error: Exception | None = None
    """
    Исключение, возникшее в аспекте. Заполняется только для события "on_error".
    Для всех остальных событий — None.

    Позволяет плагину-наблюдателю получить информацию об ошибке:
    тип исключения, сообщение, traceback через __traceback__.
    """

    has_action_handler: bool = False
    """
    Наличие подходящего обработчика @on_error на уровне Action.

    True если Action имеет @on_error обработчик, который подходит
    по типу исключения (isinstance). False если обработчик не найден
    и ошибка будет проброшена наружу.

    Заполняется только для события "on_error". Для остальных — False.

    Позволяет плагину различать:
    - Ошибки, которые Action обработает самостоятельно (has_action_handler=True).
    - Ошибки, которые полетят наружу необработанными (has_action_handler=False).
    """
