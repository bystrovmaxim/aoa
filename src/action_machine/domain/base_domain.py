# src/action_machine/domain/base_domain.py
"""
Модуль: BaseDomain — абстрактный базовый класс для всех доменов ActionMachine.

═══════════════════════════════════════════════════════════════════════════════
НАЗНАЧЕНИЕ
═══════════════════════════════════════════════════════════════════════════════

BaseDomain — абстрактный базовый класс (ABC), от которого наследуются все
домены в системе. Домен — это типизированный маркер принадлежности действия
к бизнес-области. Он не содержит логики, не хранит состояния и не имеет
методов помимо валидации.

Наследование от ABC подчёркивает, что BaseDomain нельзя использовать
напрямую — только через конкретные подклассы с определённым ``name``.

Единственный обязательный атрибут — ``name`` (ClassVar[str]). Это уникальное
строковое имя бизнес-области, которое используется:
- В графе GateCoordinator как имя узла типа "domain".
- В логах и интроспекции для идентификации домена.
- В фильтрах плагинов для маршрутизации событий по доменам.

═══════════════════════════════════════════════════════════════════════════════
ИНВАРИАНТЫ
═══════════════════════════════════════════════════════════════════════════════

1. ``name`` — обязательный атрибут. Класс без ``name`` или с пустым ``name``
   вызывает ``ValueError`` при создании подкласса (в ``__init_subclass__``).

2. ``name`` — строка (str). Нестроковый тип вызывает ``TypeError``.

3. ``name`` — непустая строка после strip(). Пробельные строки ("  ")
   считаются пустыми и вызывают ``ValueError``.

4. BaseDomain сам по себе не проверяется — валидация срабатывает только
   при создании конкретных подклассов.

5. Промежуточные абстрактные подклассы (без ``name`` в ``__dict__``,
   но добавляющие общие методы или атрибуты) допускаются — валидация
   пропускается, если ``name`` определён выше в MRO.

═══════════════════════════════════════════════════════════════════════════════
АРХИТЕКТУРА
═══════════════════════════════════════════════════════════════════════════════

    class BaseDomain(ABC):
        name: ClassVar[str]             ← обязательный атрибут
        __init_subclass__()             ← валидация при наследовании

    class OrdersDomain(BaseDomain):
        name = "orders"                 ← конкретный домен

    class CrmDomain(BaseDomain):
        name = "crm"                    ← другой домен

    # Домен — это класс. Не экземпляр, не строка.
    # Используется как тип: @meta(domain=OrdersDomain)

═══════════════════════════════════════════════════════════════════════════════
ПОЧЕМУ КЛАСС, А НЕ СТРОКА
═══════════════════════════════════════════════════════════════════════════════

Строковые домены ("orders", "crm") страдают от трёх проблем:

1. ОПЕЧАТКИ НЕ ОБНАРУЖИВАЮТСЯ. Строка "ordres" вместо "orders" —
   молчаливый баг, обнаруживаемый только при анализе логов или графа.
   Класс OrdersDomain при опечатке → ImportError при импорте.

2. IDE НЕ ПОМОГАЕТ. Строки не автодополняются. Класс — автодополняется.

3. РЕФАКТОРИНГ НЕ РАБОТАЕТ. Переименование строки "orders" → "order"
   требует ручного поиска по всему коду. Переименование класса
   OrdersDomain → OrderDomain — одна команда в IDE.

Класс-домен решает все три проблемы. Ценой одной строчки кода
(``class OrdersDomain(BaseDomain): name = "orders"``) получаем
типобезопасность, автодополнение и рефакторинг.

═══════════════════════════════════════════════════════════════════════════════
ПОЧЕМУ КЛАСС, А НЕ ЭКЗЕМПЛЯР
═══════════════════════════════════════════════════════════════════════════════

Домен — декларативная сущность, а не объект с жизненным циклом.
Он не создаётся, не уничтожается, не хранит состояние. Использование
класса (а не экземпляра) подчёркивает эту декларативность:

    @meta(domain=OrdersDomain)      ← передаём класс, не экземпляр
    class CreateOrderAction(...):
        ...

Экземпляр (OrdersDomain()) создавал бы ложное впечатление, что домен —
это объект с жизненным циклом. Класс — нет.

═══════════════════════════════════════════════════════════════════════════════
ПОЧЕМУ ABC
═══════════════════════════════════════════════════════════════════════════════

Наследование от ABC (Abstract Base Class) выполняет две функции:

1. СЕМАНТИЧЕСКАЯ МАРКИРОВКА. ABC явно сообщает разработчику: «этот класс
   не предназначен для прямого использования». Документация в коде.

2. СОВМЕСТИМОСТЬ С БУДУЩИМИ АБСТРАКТНЫМИ МЕТОДАМИ. Если в будущем
   потребуется добавить абстрактный метод (например, get_description()),
   ABC уже в MRO — не потребуется менять существующие домены.

═══════════════════════════════════════════════════════════════════════════════
ПРИМЕР ОПРЕДЕЛЕНИЯ ДОМЕНОВ
═══════════════════════════════════════════════════════════════════════════════

    class OrdersDomain(BaseDomain):
        name = "orders"

    class CrmDomain(BaseDomain):
        name = "crm"

    class WarehouseDomain(BaseDomain):
        name = "warehouse"

    class PaymentsDomain(BaseDomain):
        name = "payments"

    class NotificationsDomain(BaseDomain):
        name = "notifications"

═══════════════════════════════════════════════════════════════════════════════
ПРИМЕР ПРОМЕЖУТОЧНОГО АБСТРАКТНОГО ДОМЕНА
═══════════════════════════════════════════════════════════════════════════════

    # Промежуточный класс без name — допускается.
    # Добавляет общие атрибуты для группы доменов.
    class ExternalServiceDomain(BaseDomain):
        is_external = True

    # Конкретные домены задают name:
    class StripeDomain(ExternalServiceDomain):
        name = "stripe"

    class TwilioDomain(ExternalServiceDomain):
        name = "twilio"

═══════════════════════════════════════════════════════════════════════════════
ПРИМЕР ОШИБОК
═══════════════════════════════════════════════════════════════════════════════

    # Без name → ValueError при определении класса
    class BadDomain(BaseDomain):
        pass
    # ValueError: Класс 'BadDomain' наследует BaseDomain,
    # но не определяет атрибут 'name'.

    # Пустой name → ValueError
    class EmptyDomain(BaseDomain):
        name = ""
    # ValueError: Атрибут 'name' класса 'EmptyDomain' не может быть
    # пустой строкой.

    # Нестроковый name → TypeError
    class IntDomain(BaseDomain):
        name = 42
    # TypeError: Атрибут 'name' класса 'IntDomain' должен быть строкой,
    # получен int.

    # Пробельный name → ValueError
    class SpaceDomain(BaseDomain):
        name = "   "
    # ValueError: Атрибут 'name' класса 'SpaceDomain' не может быть
    # пустой строкой.
"""

from __future__ import annotations

from abc import ABC
from typing import Any, ClassVar


class BaseDomain(ABC):
    """
    Абстрактный базовый класс для всех доменов ActionMachine.

    Домен — типизированный маркер принадлежности действия к бизнес-области.
    Конкретные домены создаются наследованием и определяют атрибут ``name``.

    Наследует ABC, подчёркивая, что BaseDomain не предназначен для прямого
    использования — только через конкретные подклассы.

    Валидация ``name`` выполняется в ``__init_subclass__`` при создании
    подкласса. Это означает, что ошибка обнаруживается в момент определения
    класса (при загрузке модуля), а не при первом использовании в рантайме.

    Атрибуты уровня класса:
        name : ClassVar[str]
            Уникальное строковое имя бизнес-области. Обязательно для всех
            конкретных (листовых) доменов. Используется как идентификатор
            в графе, логах и фильтрах. Промежуточные абстрактные подклассы
            могут не определять ``name`` — валидация пропускается.
    """

    name: ClassVar[str]

    def __init_subclass__(cls, **kwargs: Any) -> None:
        """
        Вызывается Python при создании любого подкласса BaseDomain.

        Выполняет валидацию атрибута ``name``:
        1. Проверяет наличие ``name`` в ``cls.__dict__`` (не унаследованного).
           Если ``name`` отсутствует в текущем классе, проверяет MRO:
           если ``name`` определён в промежуточном родителе (не BaseDomain) —
           класс считается промежуточным абстрактным и валидация пропускается.
        2. Проверяет, что ``name`` — строка (str).
        3. Проверяет, что ``name`` — непустая строка после strip().

        Аргументы:
            **kwargs: аргументы, передаваемые в ``type.__init_subclass__``.

        Исключения:
            ValueError:
                - ``name`` не определён ни в подклассе, ни в его
                  промежуточных родителях (кроме BaseDomain).
                - ``name`` — пустая строка или строка из пробелов.
            TypeError:
                - ``name`` — не строка (int, list, None и т.д.).
        """
        super().__init_subclass__(**kwargs)

        # ── Проверка наличия name ──────────────────────────────────────
        #
        # Если name определён в текущем классе — валидируем его.
        # Если name НЕ определён в текущем классе — проверяем, есть ли
        # он в каком-либо промежуточном родителе (не BaseDomain).
        # Если есть — это промежуточный абстрактный класс, пропускаем.
        # Если нет нигде — это листовой класс без name, ошибка.

        if "name" not in cls.__dict__:
            for base in cls.__mro__[1:]:
                if base is BaseDomain:
                    continue
                if "name" in base.__dict__:
                    # name определён в промежуточном родителе —
                    # текущий класс наследует его, валидация не нужна.
                    return

            # name не найден ни в текущем классе, ни в промежуточных
            # родителях. Это листовой класс без name — ошибка.
            raise ValueError(
                f"Класс '{cls.__name__}' наследует BaseDomain, "
                f"но не определяет атрибут 'name'. "
                f"Укажите имя домена: name = \"my_domain\"."
            )

        raw_name = cls.__dict__["name"]

        # ── Проверка типа ──────────────────────────────────────────────
        if not isinstance(raw_name, str):
            raise TypeError(
                f"Атрибут 'name' класса '{cls.__name__}' должен быть "
                f"строкой (str), получен {type(raw_name).__name__}: {raw_name!r}."
            )

        # ── Проверка непустоты ─────────────────────────────────────────
        if not raw_name.strip():
            raise ValueError(
                f"Атрибут 'name' класса '{cls.__name__}' не может быть "
                f"пустой строкой. Укажите имя домена: name = \"my_domain\"."
            )
