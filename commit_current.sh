#!/bin/bash
# commit_current.sh - Сохраняем текущее состояние в git

echo "📦 Сохраняем текущее состояние в git..."
echo "========================================"

# 1. Проверяем, что мы в git репозитории
if ! git rev-parse --git-dir > /dev/null 2>&1; then
    echo "❌ Ошибка: это не git репозиторий"
    exit 1
fi

# 2. Смотрим текущий статус
echo "📊 Текущий статус:"
git status -s
echo ""

# 3. Добавляем все изменения (включая новые файлы)
echo "➕ Добавляем все изменения..."
git add -A

# 4. Создаём коммит с текущим состоянием
TIMESTAMP=$(date +"%Y%m%d_%H%M%S")
COMMIT_MSG="WIP: текущее состояние проекта перед массовыми исправлениями $TIMESTAMP"

echo "📝 Создаём коммит: $COMMIT_MSG"
git commit -m "$COMMIT_MSG"

# 5. Проверяем результат
echo ""
echo "✅ Коммит создан!"
git log -1 --oneline

echo ""
echo "📌 Теперь можно безопасно запускать скрипт исправлений"
echo "   Если что-то пойдёт не так, откатиться можно командой:"
echo "   git reset --hard HEAD~1"